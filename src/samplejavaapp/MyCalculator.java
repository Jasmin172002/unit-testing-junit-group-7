public class MyCalculator {
    
    // Addition
    public int AddFunction(int a, int b){
        return a + b;
    }
    
    // Subtraction
    public int SubtractFunction(int a, int b){
        return a - b;
    }
    
    // Multiplication
    public int MultiplyFunction(int a, int b){
        return a * b;
    }
    
    // Division
    public int DivideFunction(int a, int b){
        return a / b;
    }

}